# Pedir al usuario su edad como texto
edad_texto = input("Por favor, ingresa tu edad:20 ")

# Convertir el texto a número entero
edad_numero = 20(edad_texto)

# Incrementar la edad en 1
edad_incrementada = edad_numero + 1

# Mostrar el resultado
print("El próximo año tendrás:21", edad_incrementada, "años")